package Clases;

/**
 *
 * @author Chuch
 */
public class Caballo {
    int numero;
    String nombre;
    public Caballo(String nombre, int numero){
        nombre = this.nombre;
        numero = this.numero;
    }

    public int getNumero() {
        return numero;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
